private int obtenerIdValePorUsuario(int idUsuario) {
    String sql = "SELECT id_vale FROM vales WHERE id_usuario = ? ORDER BY id_vale DESC LIMIT 1";

    try (Connection con = Conexion.getConnection();
         PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setInt(1, idUsuario);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            return rs.getInt("id_vale");
        }

    } catch (SQLException e) {
        System.out.println("Error al obtener id_vale: " + e.getMessage());
    }

    return -1;
}

private void actualizarSaldoVale(int idVale, BigDecimal montoPago) {

    String sql = "UPDATE vales SET saldo_actual = saldo_actual - ? WHERE id_vale = ?";

    try (Connection con = Conexion.getConnection();
         PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setBigDecimal(1, montoPago);
        ps.setInt(2, idVale);
        ps.executeUpdate();

    } catch (SQLException e) {
        System.out.println("Error al actualizar el saldo: " + e.getMessage());
    }
}


