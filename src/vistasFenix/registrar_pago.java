    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: String clienteSeleccionado = (String) jComboBox1.getSelectedItem();
     //Obtener cliente seleccionado
    String clienteSeleccionado = (String) jComboBox1.getSelectedItem();
    if (clienteSeleccionado == null) {
        JOptionPane.showMessageDialog(this, "Selecciona un cliente.");
        return;
    }

    //Obtener ID de usuario desde ValeDAO
    ValeDAO valeDAO = new ValeDAO();
    int idUsuario = valeDAO.obtenerIdUsuarioPorNombre(clienteSeleccionado);

    if (idUsuario == -1) {
        JOptionPane.showMessageDialog(this, "No se encontró el ID del usuario.");
        return;
    }

    //Obtener el ID del vale asociado
    int idVale = obtenerIdValePorUsuario(idUsuario);
    if (idVale == -1) {
        JOptionPane.showMessageDialog(this, "El usuario no tiene vales registrados.");
        return;
    }

    //Validar monto de pago
    String montoTexto = jTextField1.getText().trim();
    if (montoTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingresa un monto de pago.");
        return;
    }

    BigDecimal montoPago;
    try {
        montoPago = new BigDecimal(montoTexto);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Monto inválido.");
        return;
    }

    //Registrar el pago
    PagoDAO pagoDAO = new PagoDAO();
    Pago pago = new Pago(idVale, montoPago, LocalDate.now());


    if (pagoDAO.registrarPago(pago)) {
        //Actualizar saldo
        actualizarSaldoVale(idVale, montoPago);

        JOptionPane.showMessageDialog(this, "Pago registrado correctamente.");
        jTextField1.setText(""); 
    } else {
        JOptionPane.showMessageDialog(this, "Error al registrar el pago.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

